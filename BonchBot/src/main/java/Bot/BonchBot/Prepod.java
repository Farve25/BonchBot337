package Bot.BonchBot;

public class Prepod {
	String name;
	String dept;
	String mail;
	String address;
	String number;

	public Prepod(String name, String dept, String mail, String address, String number) {
		this.name = name;
		this.dept = dept;
		this.mail = mail;
		this.address = address;
		this.number = number;
	}

	public Prepod(String name) {
		this.name = name;
	}
}
